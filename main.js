    function autoPlay () {
        if(intervale) {
            clearInterval(intervale);
            intervale = null;
        } else {
            intervale = setInterval(() => {
                item.scrollLeft += 250;
                if(item.scrollLeft === 1780) {
                    item.scrollLeft = 0;
                }
            }, 4000);
        }
    }
    autoPlay();

    nxtBtn[i].addEventListener('click', () => {
        //item.scrollLeft += containerWidth;
        item.scrollLeft += 250;
        if(item.scrollLeft >= 1780) {
            item.scrollLeft = 0;
        }
        clearInterval(intervale);
        intervale = null;
        autoPlay();
    })

